class AccountFragment : Fragment() {

    override fun onCreateView(inflater: LayoutInflater, container: ViewGroup?, savedInstanceState: Bundle?): View? {
        return inflater.inflate(R.layout.fragment_account, container, false)
    }

    override fun onViewCreated(view: View, savedInstanceState: Bundle?) {
        super.onViewCreated(view, savedInstanceState)

        setting.setOnClickListener {
            startActivityForResult(Intent(requireContext(), SettingActivity::class.java), 100)
        }
    }

    override fun onActivityResult(requestCode: Int, resultCode: Int, data: Intent?) {
        super.onActivityResult(requestCode, resultCode, data)
        if (requestCode == 100 && resultCode == Activity.RESULT_OK && data?.getSerializableExtra("locale") != null) {
            SwitchLanguageUtils.startSwitchLanguage(requireActivity(), data.getSerializableExtra("locale") as Locale, 1000)
        }
    }
}
